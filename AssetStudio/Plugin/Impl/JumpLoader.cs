    public Stream ProcessFile(Stream file, string filename)
    {
        var buffer = new byte[file.Length];
        file.CheckedRead(buffer);

        var bufferSpan = buffer.AsSpan();
        
        var rem = bufferSpan.Length % 8;
        var count = bufferSpan.Length / 8;

        var type = GetUsedKey(bufferSpan[..8]);
        Debug.Assert(type != KeyType.Invalid, "type != KeyType.Invalid");

        var key = (type == KeyType.Old ? OldKey : NewKey).AsSpan();
        for (int i = 0; i < count; i++)
        {
            bufferSpan.As<ulong>()[i] ^= key.As<ulong>()[i % (key.Length / 8)];
        }

        if (rem > 0)
        {
            for (int i = count * 8; i < bufferSpan.Length; i++)
            {
                bufferSpan[i] ^= key[i % key.Length];
            }
        }

        return new MemoryStream(buffer);
    }

    public bool CanProcessFile(Stream file, string filename)
    {
        var buffer = (stackalloc byte[8]);
        file.CheckedRead(buffer);

        return GetUsedKey(buffer) != KeyType.Invalid;
    }

    private static KeyType GetUsedKey(Span<byte> header)
    {
        var known = "UnityFS\0"u8;
        var temp = (stackalloc byte[8]);
        temp.As<ulong>()[0] = header.As<ulong>()[0] ^ known.As<ulong>()[0];

        if (temp.SequenceEqual(OldKey.AsSpan(..8)))
            return KeyType.Old;

        if (temp.SequenceEqual(NewKey.AsSpan(..8)))
            return KeyType.New;

        return KeyType.Invalid;
    }

    private enum KeyType
